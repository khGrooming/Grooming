CREATE OR REPLACE VIEW VIEW_MESSAGE AS
SELECT MESSAGE_NO, FROM_MEMBER_NO, MA.MEMBER_NICKNAME FROM_MEMBER_NICKNAME, MA.MEMBER_PHOTO FROM_MEMBER_PHOTO
	, TO_MEMBER_NO, MB.MEMBER_NICKNAME TO_MEMBER_NICKNAME, MB.MEMBER_PHOTO TO_MEMBER_PHOTO
	, MESSAGE_CONTENT, MESSAGE_DATE, MESSAGE_CONFIRM
FROM MESSAGE MSG
LEFT JOIN MEMBER MA ON (MA.MEMBER_NO = MSG.FROM_MEMBER_NO)
LEFT JOIN MEMBER MB ON (MB.MEMBER_NO = MSG.TO_MEMBER_NO)
ORDER BY MESSAGE_DATE DESC;

